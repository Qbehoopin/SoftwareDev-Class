# What Are Errors?
# Errors are problems in a program that prevent it from running as expected.
# In Python, common types of errors include syntax errors, runtime errors, and logical errors.


# Example of a Syntax Error
# This error occurs when the code is not written in proper Python syntax.
# Example: print("Hello, World!" , missing closing parenthesis

# Example of a Runtime Error
# These errors occur when the code runs but fails during execution.
# Example: Dividing by zero, 10/0 (raises ZeroDivisionError)

# Example of a Logical Error
# The program runs without crashing, but it produces incorrect results.
# Example: Adding instead of multiplying two numbers.